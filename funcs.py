def title_name():
    """
    Функция делает все буквы заглавными
    :return: None
    """
    user_input = input()
    print(user_input.lower())

def swapcase_name():
    """
    Функция делает первые буквы слов заглавными
    :return: None
    """
    user_input_1 = input()
    print(user_input_1.swapcase())